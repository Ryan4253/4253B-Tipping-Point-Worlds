void autonomous() {
    
    /** Initialization */
    leftDrive.setBrakeMode(AbstractMotor::brakeMode::brake);
    rightDrive.setBrakeMode(AbstractMotor::brakeMode::brake);
    topBranchController->setTarget(TopBranchPosition::REST);
    liftController->setTarget(0);
    clamp.set(true);

    goto SimpleAuton;

    SimpleAuton:

    /** --------- */
    profiler->setTarget(-2.15_ft, true);
    backClamp.set(true); pros::delay(250); tilter.set(true);
    turnToAngle(110_deg);

    liftController->setTarget(LiftPosition::LOADINGHEIGHT);
    // profiler->setTarget(9.75_ft);
    //profiler->setTarget(path2);
    profiler->setTarget(3.25_ft);
    roller.moveVoltage(12000);
    pros::delay(850);
    liftController->setTarget(0);
    pros::delay(750);
    //clamp.set(false);
    profiler->waitUntilSettled();
    clamp.set(false);
    
    liftController->setTarget(LiftPosition::MAXHEIGHT);
    turnToAngle(95_deg, true);
    profiler->setTarget(5_ft, true);
    leftDrive.moveVoltage(-12000); rightDrive.moveVoltage(12000); pros::delay(300); leftDrive.moveVoltage(-0); rightDrive.moveVoltage(0);
    clamp.set(true);
    


    profiler->setTarget(-1_ft, true);
    liftController->setTarget(0);
    turnToAngle(345_deg);
    tilter.set(false); backClamp.set(false);
    roller.moveVoltage(1000);
    profiler->setTarget(5_ft, true);

    profiler->setTarget(-0.5_ft, true);
    turnToAngle(-89_deg);
    //profiler->setTarget(-6_ft, true);
    leftDrive.moveVoltage(-12000);
    rightDrive.moveVoltage(-12000);
    pros::delay(2000);
    while(true){pros::delay(10);}

    TallBranchAuton:
    
    /** Push neutral mogo back & collect rings */
    profiler->setTarget(-2.5_ft, true);
    topBranchController->setTarget(TopBranchPosition::LOADINGHEIGHT);
    pros::delay(250);
    liftController->setTarget(LiftPosition::LOADINGHEIGHT);
    pros::delay(750);
    profiler->setTarget(26_in, true);
    leftNeedle.set(true); rightNeedle.set(true);

    /** Align with tall neutral mogo */
    liftController->setTarget(LiftPosition::TOPBRANCH);
    topBranchController->setTarget(-300);
    profiler->setTarget(path1, true);
    turnToAngle(0_deg);
    profiler->setTarget(0.405_ft, true);
    turnToAngle(270_deg);
    // profiler->setTarget(-0.75_ft, true);
    leftDrive.moveVelocity(75); rightDrive.moveVelocity(75); pros::delay(1000); leftDrive.moveVelocity(0); rightDrive.moveVelocity(0);
    
    /** Deploy rings */
    backClamp.set(true); pros::delay(250); tilter.set(true);
    topBranchController->setTarget(TopBranchPosition::TOPBRANCH);
    topBranchController->waitUntilSettled();
    pros::delay(500);
    leftNeedle.set(false); rightNeedle.set(false);

    pros::delay(250);
    
    /** Oscillate */
    for(int i = 0; i < 4; i++) {
        leftDrive.moveVoltage(11000); rightDrive.moveVoltage(-11000);
        pros::delay(400);
        leftDrive.moveVoltage(-11000); rightDrive.moveVoltage(11000);
        pros::delay(400);
    }
    leftDrive.moveVoltage(0); rightDrive.moveVoltage(0);
    pros::delay(999099);
}

